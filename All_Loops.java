public class Main
{
	public static void main(String[] args) {
		System.out.println("------------");
		System.out.println("For Loop");
		System.out.println("------------");
		for(int i=1;i<=10;i++){
		    System.out.println(i);
		}
		System.out.println("------------");
		System.out.println("While Loop");
		System.out.println("------------");
		int j=1;
		while(j<=10){
		    System.out.println(j);
		    j++;
		}
		System.out.println("---------------");
		System.out.println("Do While Loop");
		System.out.println("---------------");
		int num=1;
		do{
		    System.out.println(num);
		    num++;
		}while (num<=10);
		
	}
}
