public class Main
{
	public static void main(String[] args) {
		System.out.println("Increment & Decrement Operator");
		System.out.println("---------------------");
		int num=5;
		System.out.println(num++);
		System.out.println(--num);
		System.out.println(num++);
		System.out.println(num--);
		System.out.println(++num);
	}
}
